create database Netflix_Original;
use Netflix_Original;

create table Title(
ID_TITLE int auto_increment,
TITULOS varchar(500) NOT NULL,
primary key (ID_TITLE)
);

create table Genre(
ID_GENERO int auto_increment,
GENERO varchar(250) not null,
primary key (ID_GENERO)
);

create table IMDB_Score (
ID_IMDB INT auto_increment,
AVALIACAO FLOAT NOT NULL,
primary key (ID_IMDB)
);

create table Linguagem (
ID_LINGUAGEM INT auto_increment,
LINGUAGEM varchar(50) NOT NULL,
primary key (ID_LINGUAGEM)
);

create table Filmes (
ID_TITLE int not null,
ID_GENRE int not null,
ID_IMDB int not null,
ID_Linguagem int not null,

constraint foreign key (ID_GENRE) references Genre(ID_GENERO),
constraint foreign key (ID_IMDB) references IMDB_Score(ID_IMDB),
constraint foreign key (ID_TITLE) references Title(ID_TITLE),
constraint foreign key (ID_LINGUAGEM) references Linguagem(ID_LINGUAGEM)
);


