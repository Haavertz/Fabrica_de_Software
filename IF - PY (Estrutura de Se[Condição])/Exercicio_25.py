#Digitar uma letra e mostrar se é vogal ou consoante

letra = input("Digite uma LETRA: ")
letra = letra.upper()

if (letra == 'A' ):
    print ("Essa letra é VOGAL")
elif (letra == 'E'):
    print ("Essa letra é VOGAL")
elif (letra == 'I'):
    print ("Essa letra é VOGAL")
elif (letra == 'O'):
    print ("Essa letra é VOGAL")
elif (letra == 'U'):
    print ("Essa letra é VOGAL!")
else:
    print ("Essa letra é uma CONSOANTE!")
